public class HospSearchDoc {

	// 职称
	private String doctorTitle;

	// 医院等级
	private String hospLevel;

	// 描述
	private String details;

	private String province_code;

	private String city_code;

	private String area_code;

	private String One_level_dept;

	private String Two_level_dept;

	public String getDoctorTitle() {
		return doctorTitle;
	}

	public void setDoctorTitle(String doctorTitle) {
		this.doctorTitle = doctorTitle;
	}

	public String getHospLevel() {
		return hospLevel;
	}

	public void setHospLevel(String hospLevel) {
		this.hospLevel = hospLevel;
	}

	public String getDetails() {
		return details;
	}

	public void setDetails(String details) {
		this.details = details;
	}

	public String getProvince_code() {
		return province_code;
	}

	public void setProvince_code(String province_code) {
		this.province_code = province_code;
	}

	public String getCity_code() {
		return city_code;
	}

	public void setCity_code(String city_code) {
		this.city_code = city_code;
	}

	public String getArea_code() {
		return area_code;
	}

	public void setArea_code(String area_code) {
		this.area_code = area_code;
	}

	public String getOne_level_dept() {
		return One_level_dept;
	}

	public void setOne_level_dept(String one_level_dept) {
		One_level_dept = one_level_dept;
	}

	public String getTwo_level_dept() {
		return Two_level_dept;
	}

	public void setTwo_level_dept(String two_level_dept) {
		Two_level_dept = two_level_dept;
	}

	

}
